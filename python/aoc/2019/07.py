from itertools import permutations

from .util import argmax, Machine


def throut(c, ps, i=0):
    o = i
    for p in ps:
        m = Machine(c.copy())
        m.run(inp=[p, o])
        o = m.oq.popleft()
    return o

def throutcl(c, ps, ii=0):
    o = ii
    ms = []
    for (i, p) in enumerate(ps):
        m = Machine(c.copy())
        ms.append(m)
        if i != 0:
            m.iq = ms[i-1].oq
        m.iq.append(p)
    m.oq = ms[0].iq
    ms[0].iq.append(o)
    while True:
        for m in ms:
            m.resume()
        o_new = ms[0].iq[0]
        if o_new == o:
            return o
        o = o_new

